<section class="jenis-kanker-section">
  <h2>Jenis-Jenis Kanker</h2>
  <p>
    Kanker dapat menyerang hampir semua bagian tubuh manusia. Berikut adalah beberapa jenis kanker yang umum terjadi, beserta penjelasannya:
  </p>

  <h3>Kanker Payudara</h3>
  <p>
    Kanker ini terjadi ketika sel-sel di jaringan payudara tumbuh secara abnormal. Biasanya dimulai dari saluran penghasil susu atau lobulus. Deteksi dini melalui SADARI dan mammografi sangat penting.
  </p>

  <h3>Kanker Serviks</h3>
  <p>
    Jenis kanker ini menyerang leher rahim (serviks) dan seringkali disebabkan oleh infeksi HPV. Vaksinasi HPV dan tes Pap smear berkala dapat membantu mencegah kanker serviks.
  </p>

  <h3>Kanker Paru-Paru</h3>
  <p>
    Kanker paru-paru paling sering dikaitkan dengan kebiasaan merokok. Gejalanya meliputi batuk berkepanjangan, sesak napas, dan nyeri dada.
  </p>

  <h3>Kanker Prostat</h3>
  <p>
    Umumnya terjadi pada pria usia lanjut. Kanker ini berkembang di kelenjar prostat dan bisa tumbuh sangat lambat tanpa menimbulkan gejala awal.
  </p>

  <h3>Kanker Kolorektal</h3>
  <p>
    Merupakan kanker yang berkembang di usus besar atau rektum. Pola makan rendah serat dan tinggi lemak dapat meningkatkan risikonya.
  </p>

  <h3>Kanker Hati</h3>
  <p>
    Kanker hati seringkali berkembang dari kondisi medis seperti hepatitis B/C kronis atau sirosis. Deteksi dini sangat penting karena gejala sering tidak terlihat pada awalnya.
  </p>

  <h3>Kanker Darah (Leukemia)</h3>
  <p>
    Leukemia adalah kanker yang memengaruhi sel-sel darah dan sumsum tulang. Gejalanya bisa meliputi kelelahan ekstrem, infeksi berulang, dan memar mudah.
  </p>

  <h3>Kanker Otak</h3>
  <p>
    Kanker ini terjadi saat pertumbuhan sel abnormal di otak atau jaringan sekitarnya. Gejalanya tergantung pada lokasi tumor, seperti sakit kepala, gangguan penglihatan, atau kesulitan berbicara.
  </p>

  <h3>Kanker Limfoma</h3>
  <p>
    Jenis kanker ini menyerang sistem limfatik. Ada dua tipe utama: Hodgkin dan non-Hodgkin. Limfoma biasanya ditandai dengan pembengkakan kelenjar getah bening.
  </p>

  <h3>Kanker Kulit</h3>
  <p>
    Kanker kulit, termasuk melanoma, biasanya disebabkan oleh paparan sinar ultraviolet (UV) yang berlebihan. Pencegahan dapat dilakukan dengan menggunakan pelindung matahari.
  </p>
