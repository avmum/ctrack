  <section class="risk-section">
  <h2>Faktor Risiko Kanker</h2>
  <p>
    Kanker dapat disebabkan oleh berbagai faktor risiko yang dapat dibagi menjadi dua kategori utama: faktor yang dapat dikendalikan (modifiable) dan faktor yang tidak dapat dikendalikan (non-modifiable). Memahami faktor-faktor ini sangat penting untuk pencegahan dan deteksi dini.
  </p>

  <h3>1. Faktor Risiko yang Dapat Dikendalikan</h3>
  <ul class="risk-list">
    <li><strong>Merokok:</strong> Merupakan penyebab utama berbagai jenis kanker, terutama kanker paru-paru, mulut, dan tenggorokan.</li>
    <li><strong>Konsumsi Alkohol Berlebihan:</strong> Dapat meningkatkan risiko kanker hati, mulut, tenggorokan, dan payudara.</li>
    <li><strong>Pola Makan Tidak Sehat:</strong> Konsumsi tinggi lemak jenuh, daging olahan, serta rendah serat dapat memicu kanker.</li>
    <li><strong>Kelebihan Berat Badan atau Obesitas:</strong> Dapat meningkatkan risiko kanker usus besar, payudara (pasca menopause), dan rahim.</li>
    <li><strong>Kurang Aktivitas Fisik:</strong> Gaya hidup sedentari juga berkontribusi terhadap risiko kanker tertentu.</li>
    <li><strong>Paparan Sinar Matahari Berlebihan:</strong> Tanpa perlindungan, dapat meningkatkan risiko kanker kulit seperti melanoma.</li>
    <li><strong>Infeksi Tertentu:</strong> HPV, Hepatitis B dan C, serta H. pylori dapat memicu kanker serviks, hati, dan lambung.</li>
  </ul>

  <h3>2. Faktor Risiko yang Tidak Dapat Dikendalikan</h3>
  <ul class="risk-list">
    <li><strong>Usia:</strong> Risiko kanker meningkat seiring bertambahnya usia.</li>
    <li><strong>Riwayat Keluarga:</strong> Adanya anggota keluarga dengan riwayat kanker dapat menunjukkan predisposisi genetik.</li>
    <li><strong>Faktor Genetik:</strong> Mutasi pada gen tertentu (seperti BRCA1 dan BRCA2) meningkatkan risiko kanker payudara dan ovarium.</li>
    <li><strong>Jenis Kelamin:</strong> Beberapa kanker lebih umum pada jenis kelamin tertentu, misalnya kanker prostat pada pria dan kanker serviks pada wanita.</li>
    <li><strong>Lingkungan:</strong> Paparan jangka panjang terhadap zat karsinogenik di tempat kerja atau lingkungan sekitar.</li>
  </ul>
</section>

